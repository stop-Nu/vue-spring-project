<template>
    <div class = "main-map" ref = "map">
    </div>
</template>

<script>
import OlLayerTile from 'ol/layer/Tile.js';
import OlView from 'ol/View.js';
import OlMap from 'ol/Map.js';
import OSM from 'ol/source/OSM';
import {fromLonLat} from 'ol/proj.js'

export default {
    name : 'MainMap',
    data(){
        return {
            olMap:undefined,
        }
    },
    mounted() {
        this.olMap = new OlMap({
            target : this.$refs.map,
            layers:[
                new OlLayerTile({
                    source: new OSM()
                })
            ],
            view : new OlView({
                center : fromLonLat([126.8868688, 37.5730739]),
                zoom : 10
            })
        })
    },
}
</script>

<style scoped>
.main-map {
  width: 100%;
  height: 100%;
}

</style>